    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String idobat = txtIDObat.getText();
        String namaobat = txtIDObat.getText();
        String keterangan = txtIDObat.getText();
        String kategori = txtKategori.getText();
        String stok = txtStok.getText();

        if(checkID(idobat)){
            JOptionPane.showMessageDialog(null, "ID Obat sudah ada", "Error", 2);
        }
        else if(idobat.trim().equals("") || namaobat.trim().equals("") || keterangan.trim().equals("")
            || kategori.trim().equals("") || stok.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Terdapat Data yang Kosong", "Error", 2);
        } else{
            int answer = JOptionPane.showOptionDialog(this, "Yakin simpan data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if(answer==JOptionPane.YES_OPTION)
            {
                try {
                    String sql = "insert into farmasi values('"
                    +txtIDObat.getText()+"','"
                    +txtNamaObat.getText()+"','"
                    +txtKeterangan.getText()+"','"
                    +txtKategori.getText()+"','"
                    +txtStok.getText()+"')";
                    java.sql.Connection con = (java.sql.Connection)sisteminformasipuskesmas.connection.getConnection();
                    java.sql.PreparedStatement pst = con.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    tabel_data();
                    clear_form();
                    txtIDObat.requestFocus();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal disimpan", "Error", 2);
                    clear_form();
                    txtIDObat.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String idobat = txtIDObat.getText();
        String namaobat = txtNamaObat.getText();
        String keterangan = txtKeterangan.getText();
        String kategori = txtKategori.getText();
        String stok = txtStok.getText();

        if(idobat.trim().equals("") || namaobat.trim().equals("") || keterangan.trim().equals("")
            || kategori.trim().equals("") || stok.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah", "Error", 2);
        }
        else if(!checkID(idobat)){
            JOptionPane.showMessageDialog(null, "Data yang akan diubah tidak ditemukan", "Error", 2);
        } else{
            int answer = JOptionPane.showOptionDialog(this, "Yakin ubah data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if(answer==JOptionPane.YES_OPTION)
            {
                try {
                    java.sql.Connection con = (java.sql.Connection)sisteminformasipuskesmas.connection.getConnection();
                    String sql = "update farmasi set id_obat='" + txtIDObat.getText()+
                    "', nama_obat='" + txtNamaObat.getText()+
                    "', keterangan='" + txtKeterangan.getText()+
                    "', kategori='" + txtKategori.getText()+
                    "', stok='" + txtStok.getText()+
                    "' where id_obat='" + txtIDObat.getText()+"'";
                    java.sql.PreparedStatement pst = con.prepareStatement(sql);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    tabel_data();
                    clear_form();
                    txtIDObat.requestFocus();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah", "Error", 2);
                    clear_form();
                    txtIDObat.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

