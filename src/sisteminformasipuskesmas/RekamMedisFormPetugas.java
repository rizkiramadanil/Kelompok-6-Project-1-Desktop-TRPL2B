    public void load_data_rekammedis(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            java.sql.Connection con = (java.sql.Connection)sisteminformasipuskesmas.connection.getConnection();
            Statement st = con.createStatement();
            
            String sql = "select * from rekam_medis";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                Object[] o = new Object[9];
                o [0] = rs.getString("id_rekammedis");
                o [1] = rs.getString("tanggal_berobat");
                o [2] = rs.getString("nomorpendaftaran");
                o [3] = rs.getString("nama_pasien");
                o [4] = rs.getString("nama_dokter");
                o [5] = rs.getString("keluhan");
                o [6] = rs.getString("diagnosa");
                o [7] = rs.getString("tindakan");
                o [8] = rs.getString("nama_obat");
                
                model.addRow(o);
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan", "Error", 2);
        }
    }
    
