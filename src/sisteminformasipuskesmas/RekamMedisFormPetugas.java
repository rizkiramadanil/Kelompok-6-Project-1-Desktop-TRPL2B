        tabel.addColumn("Tanggal Berobat");
        tabel.addColumn("Nomor Pendaftaran");
        tabel.addColumn("Nama Pasien");
        tabel.addColumn("Nama Dokter");
        tabel.addColumn("Keluhan");
        tabel.addColumn("Diagnosa");
        tabel.addColumn("Tindakan");
        tabel.addColumn("Nama Obat");
