    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String kodepetugas = txtKodePetugas.getText();
        String namapetugas = txtNamaPetugas.getText();
        String pass = String.valueOf(pwdPassword.getPassword());
        String passconf = String.valueOf(pwdPasswordConfirm.getPassword());
        
        if(verifyFields())
        {
            PreparedStatement ps;
            ResultSet rs;
            String registerUserQuery = "INSERT INTO `administrator`(`kode_petugas`, `nama_petugas`, `password`) VALUES (?,?,?)";
            
            try {
                ps = connection.getConnection().prepareStatement(registerUserQuery);
                
                ps.setString(1, kodepetugas);
                ps.setString(2, namapetugas);
                ps.setString(3, pass);
                
                if(ps.executeUpdate() != 0){
                    JOptionPane.showMessageDialog(null, "Akun Berhasil Dibuat", "Register Berhasil", 2);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Cek Data", "Register Error", 2);
                }
            } catch (SQLException ex) {
                Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    public boolean verifyFields()
    {
        String kodepetugas = txtKodePetugas.getText();
        String namapetugas = txtNamaPetugas.getText();
        String pass = String.valueOf(pwdPassword.getPassword());
        String passconf = String.valueOf(pwdPasswordConfirm.getPassword());
        
        if(kodepetugas.trim().equals("kode petugas") || namapetugas.trim().equals("nama petugas") || 
           pass.trim().equals("password") || passconf.trim().equals("password"))
        {
            JOptionPane.showMessageDialog(null, "Terdapat Data yang Kosong", "Regsiter Error",2);
            return false;
        }
        else if(!pass.equals(passconf))
        {
            JOptionPane.showMessageDialog(null, "Konfirmasi Password Salah", "Register Error",2);
            return false;
        }
        else{
            return true;
        }
    }
    
    public boolean checkKodePetugas(String kodepetugas){
        PreparedStatement st;
        ResultSet rs;
        boolean kodepetugas_exist = false;
        
        String query = "SELECT * FROM `administrator` WHERE `kode_petugas` = ?";
        
        try {
            st = connection.getConnection().prepareStatement(query);
            
            st.setString(1, kodepetugas);
            rs = st.executeQuery();
            
            if(rs.next())
            {
                kodepetugas_exist = true;
                JOptionPane.showMessageDialog(null, "Kode Petugas Sudah Digunakan", "Register Error", 2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return kodepetugas_exist;
    }
    
